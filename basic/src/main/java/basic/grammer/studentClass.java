package basic.grammer;


public class studentClass {
	private String name;
	private String sex;
	private int ID;
	
	public void setName(String name) {
		this.name=name;
	}
	public void setSex(String sex) {
		this.sex=sex;
	}
	public void setID(int ID) {
		this.ID=ID;
	}
	
	public String getName() {
		return name;
	}
	public String getSex() {
		return sex;
	}
	public int getID() {
		return ID;
	}
	
}
